package cdac;

public class P11 {

	public static void main(String[] args) {
		//11. Write the Java Program to print the following series Triangular Number Series[Solution]

			//	1 3 6 10 15 21 28 36 45 55 .....
		
		int i=1;
		int j=2;
		System.out.print("Triangular Series=="+i+" , ");
		while(j<=10) {
		    i=i+j;
			System.out.print(i+" , ");
			j++;
		
			
		}
		

	}

}
