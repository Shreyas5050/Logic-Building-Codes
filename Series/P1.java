package cdac;

public class P1 {

	public static void main(String[] args) {
		// 1. Write the Java Program to print the following series EVEN number Series[Solution]
             //2 4 6 8 10 12 14 16 .....

		
		System.out.println("Even Series");
		for(int i=1;i<=20;i++) {
		if(i%2==0) {
			System.out.print(i+ " ");
		}
		}
	}

}
